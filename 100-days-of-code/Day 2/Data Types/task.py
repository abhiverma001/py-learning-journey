print("Hello"[0])  #Pulling a particular latter of a string, this is called subscripting
print("Hello"[3])
print("Hello"[-4])

# String
print("123" + "345")

# Integer = Whole number
print(123 + 345)  #This would be calculated, because we got actual number instead of string

# Large Integers
print(123_456_789)

# Float = Floating Point Number
print(3.12413)

# Boolean
print(True)
print(False)